    private void txtCarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCarKeyTyped
        char c = evt.getKeyChar();
        if((c<'a' || c>'z') && (c<'á' || c>'ú') && (c<'A' || c>'Z' ) && (c<'Á' || c>'Ú') && (c<' ' || c>' ')) evt.consume();
    }//GEN-LAST:event_txtCarKeyTyped

