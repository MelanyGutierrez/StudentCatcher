    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        if ((txtId.getText().equals("")) || (txtCar.getText().equals("")) || (txtNom.getText().equals(""))){
            
            javax.swing.JOptionPane.showMessageDialog(this,"Llene todos los campos, por favor. \n","AVISO",javax.swing.JOptionPane.INFORMATION_MESSAGE);
            txtCar.requestFocus();
        }
        else{
        
            Cursos NE = new Cursos();
            JOptionPane.showMessageDialog(null, NE.ModificarCurso(Integer.parseInt(txtId.getText()), txtCar.getText(), txtNom.getText()));
            txtId.setText("");
            txtCar.setText("");
            txtNom.setText("");
            
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

